    private void updateTabela(){        
        DefaultTableModel model = (DefaultTableModel)tabela.getModel();        
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();        
        ProprietarioPersistencia persistencia = new ProprietarioPersistencia(); 
        try{                    
            List<Proprietario> listaProprietario  = persistencia.listar();
            for(int i=0;i < listaProprietario.size();i++){
                Proprietario item = listaProprietario.get(i);
                model.addRow( new Object[] { 
                    item.getId(), 
                    item.getNome(),
                    item.getSobrenome(),
                    item.getIdentidade()
                    
                });                
            }            
        }
        catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }

