    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Proprietario proprietario = new Proprietario();
        proprietario.setNome(txtNome.getText());
        proprietario.setSobrenome(txtSobrenome.getText());
        proprietario.setIdentidade(Integer.parseInt(txtIdentidade.getText()));
        ProprietarioPersistencia persistencia = new ProprietarioPersistencia ();
        
        try {
            persistencia.inserir(proprietario);
            updateTabela();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível inserir! ");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

